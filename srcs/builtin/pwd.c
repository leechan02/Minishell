#include "builtin.h"

int	ft_pwd(char **env)
{
	int	i;

	i = 0;
	while (env[i] != NULL)
	{
		if (ft_strncmp(env[i], "PWD=", 4) == 0)
			break ;
		i++;
	}
	printf("%s\n", env[i] + 4);
	return (TRUE);
}
